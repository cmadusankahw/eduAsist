    public void refresh()
    {
          
        try{
            databaseCon db1=new databaseCon("root","");
            db1.connect();
            
            DefaultTableModel tb=(DefaultTableModel)tbdata1.getModel();
        
             ResultSet rcount = db1.getStatement().executeQuery("SELECT count(username) FROM user_info  ;");
            
            rcount.next();
            int n=rcount.getInt(1);
             tb.setRowCount(n); 
              
             
            ResultSet rs = db1.getStatement().executeQuery("SELECT * FROM user_info;");
            int i=0;
            
            while(rs.next())
            {
                tb.setValueAt(rs.getString(1),i,0);
                tb.setValueAt(rs.getString(2), i, 1);
                tb.setValueAt(rs.getString(3), i, 2);
                i++;
            } 
           // tb.setRowCount(i);
            db1.disconnect();
        }
        catch(Exception e){
            essentials.infomsgbox("Error",e.toString());
        }
    }
    
