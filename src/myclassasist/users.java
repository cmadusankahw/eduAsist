    private void tbdata1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbdata1MouseClicked
       int row=tbdata1.getSelectedRow();
            String rc=(tbdata1.getModel()).getValueAt(row,0).toString();
            if(!"".equals(rc)){
        
        
        try{
           databaseCon db2 = new databaseCon("root","");
        
             db2.connect();
            String selque="SELECT * FROM user_info WHERE username='"+rc+"';";
            
            ResultSet rs = db2.getStatement().executeQuery(selque);
            
            
            while(rs.next())
            {
              txtuname.setText(rs.getString(1));  
              txtpword.setText(rs.getString(2));
              comboutype.setSelectedItem(rs.getString(3));
            }  
            
            txtuname.setEditable(false);
            btndel1.setEnabled(true);
            btnup1.setEnabled(true);
             db2.disconnect();
             
             
      }
      catch(Exception e){
              essentials.infomsgbox("Error",e.toString());
      }
   }
      
    }//GEN-LAST:event_tbdata1MouseClicked

