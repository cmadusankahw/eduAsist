    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
         try{
             databaseCon dbadd = new databaseCon("root","");
             String s1=txtuname.getText();
              String s2=txtpword.getText();
              String s3=(String) comboutype.getSelectedItem();
             dbadd.connect();
             
             String saveq="INSERT INTO user_info(username,password,user_type)VALUES(?,?,?);";
             dbadd.pStatement(saveq);
             
             dbadd.getPs().setString(1,s1);
              dbadd.getPs().setString(2,s2);
               dbadd.getPs().setString(3, s3);
               dbadd.getPs().execute();
               
              
   
             dbadd.disconnect();
             
              refresh();
              reset();
               essentials.infomsgbox("Success","Data added Successfully!");
             
         } 
         catch(Exception e){
              essentials.infomsgbox("Error",e.toString());
        }
        
       
                
    }//GEN-LAST:event_btnaddActionPerformed

