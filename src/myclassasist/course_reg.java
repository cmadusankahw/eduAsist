             int t1=(int)s6;
             int t2=(int)s7; 
             double td=(t2-t1) + (((s7-t2) - (s6-t1)));
             
             dbadd.connect();
             
             String saveq="INSERT INTO course_info VALUES(?,?,?,?,?,?,?,?,?,?,?);";
             dbadd.pStatement(saveq);
             
               dbadd.getPs().setString(1,s1);
               dbadd.getPs().setString(2,s2);
               dbadd.getPs().setInt(3, s3);
               dbadd.getPs().setString(4, sc1);
               dbadd.getPs().setDouble(5, s4);
               dbadd.getPs().setDouble(6, s5);
               dbadd.getPs().setString(7, sc2);
