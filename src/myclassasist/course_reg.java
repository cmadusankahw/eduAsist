    private void tbcourseMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbcourseMouseClicked
      int row=tbcourse.getSelectedRow();
            String rc=(tbcourse.getModel()).getValueAt(row,0).toString();
            if(!"".equals(rc)){   
        
        
        try{
           databaseCon db2 = new databaseCon("root","");
        
             db2.connect();
             
             
            String selque="SELECT c_id,c_title,c_duration,lec_id,c_fee,"
                    + "c_lecfee,c_day,c_time FROM course_info WHERE c_id='"+rc+"';";
            
            ResultSet rs = db2.getStatement().executeQuery(selque);
            
            
            while(rs.next())
            {
              txtcid.setText(rs.getString(1));  
              txtctitle.setText(rs.getString(2));
              txtduration.setText(rs.getString(3));
              comlec.setSelectedItem(rs.getString(4));
              txttotal.setText(rs.getString(5));
              txtlecfee.setText(rs.getString(6));
              comday.setSelectedItem(rs.getString(7));
              String t=rs.getString(8);
              txtt1.setText(t.substring(0,t.indexOf("-")-1)); 
              txtt2.setText(t.substring(t.indexOf("-")+1));
              
            }  
            
            txtcid.setEditable(false);
            btndel.setEnabled(true);
            btnup.setEnabled(true);
             db2.disconnect();
             
             
      }
      catch(Exception e){
              essentials.infomsgbox("Error",e.toString());
        }
             
}
              
    }//GEN-LAST:event_tbcourseMouseClicked

