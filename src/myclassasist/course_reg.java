    public void refresh()
    {
          DefaultTableModel tb=(DefaultTableModel)tbcourse.getModel();
        try{
            databaseCon db1=new databaseCon("root","");
            db1.connect();
            
             ResultSet rcount = db1.getStatement().executeQuery("SELECT count(c_id) FROM course_info  ;");
            
            rcount.next();
            int n=rcount.getInt(1);
             tb.setRowCount(n); 
            
            ResultSet rs = db1.getStatement().executeQuery("SELECT c_id,c_title,c_duration,lec_id,c_fee,c_lecfee,c_day,c_time,c_datereg FROM course_info;");
            int i=0;
            
            while(rs.next())
            {
               tb.setValueAt(rs.getString(1),i,0);
                tb.setValueAt(rs.getString(2), i, 1);
                tb.setValueAt(rs.getInt(3), i, 2);
               tb.setValueAt(rs.getString(4), i, 3);
               tb.setValueAt(rs.getDouble(5), i, 4);
                tb.setValueAt(rs.getDouble(6), i, 5);
               tb.setValueAt(rs.getString(7), i, 6);
               tb.setValueAt(rs.getString(8), i, 7);
               tb.setValueAt(rs.getDate(9), i, 8);
                i++;
            } 
            //tb.setRowCount(i);
            dataset();
            db1.disconnect();
        }
        catch(Exception e){
            essentials.infomsgbox("Error",e.toString());
        }
    }
    
